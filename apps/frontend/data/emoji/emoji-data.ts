// Get all emojis as a flat list
export function getAllEmojis(): string[] {
  const emojis: string[] = []
  for (const category of EMOJI_CATEGORIES) {
    emojis.push(...category.emojis)
  }
  return emojis
}

// Search emojis by keyword
export function searchEmojis(query: string): string[] {
  if (!query) return []

  const normalizedQuery = query.toLowerCase()
  const results: string[] = []

  // Search in Japanese keywords
  for (const [emoji, keywords] of Object.entries(EMOJI_KEYWORDS_JA)) {
    if (keywords.some(kw => kw.includes(normalizedQuery))) {
      results.push(emoji)
    }
  }

  // Search in category names
  for (const category of EMOJI_CATEGORIES) {
    if (category.name.includes(query) || category.nameEn.toLowerCase().includes(normalizedQuery)) {
      // Add first few emojis from matching category
      results.push(...category.emojis.slice(0, 10))
    }
  }

  // Remove duplicates
  return [...new Set(results)]
}
